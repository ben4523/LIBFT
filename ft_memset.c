#include "libft.h"

void			*ft_memset(void *s, int c, size_t n)
{
	size_t		t;
	char		*ptr;

	ptr = (char*)s;
	t = 0;
	while (n-- > 0)
		*ptr++ = (char)c;
	return (s);
}
